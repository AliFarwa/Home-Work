package HomeWork7;

public class Homework2 {
    public static void main(String[] args) {
        //Print numbers from 100 to 1
        int value = 100;
        while (value >= 1) {
            System.out.print(" " + value + " ");
            value--;
        }
        for (int number = 100; number >= 1; number--) {
            System.out.print("" + number + " ");
        }
    }
}
