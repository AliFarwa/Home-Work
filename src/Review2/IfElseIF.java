package Review2;

public class IfElseIF {
    public static void main(String[] args) {

        int worldCup = 2022;
        if (worldCup == 2022) {
            System.out.println("Host Country is Qatar");
        } else if (worldCup==2018) {
            System.out.println("Host Country is Russia");
        } else if (worldCup==2014) {
            System.out.println("Host Country is USA");
        }else {
            System.out.println("Host Country is Pakistan");
        }
    }
}
